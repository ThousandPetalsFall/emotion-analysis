/**
 * 根据答案生成报告
 * @param {Object} answers - 用户答案
 * @param {Array} recordTypes - 记录的类型标签
 * @returns {Object} 报告内容
 */
export function generateReport(answers, recordTypes) {
  // 从记录类型中选择主要的 1-2 个
  const mainTypes = recordTypes.slice(0, 2);

  // 为每个类型选择一个摘要
  const summaries = mainTypes.map(type => {
    const library = summaryLibrary[type] || summaryLibrary.unclear;
    const randomIndex = Math.floor(Math.random() * library.length);
    return library[randomIndex];
  });

  // 选择一个引导问题
  const mainType = mainTypes[0] || 'unclear';
  const questionLibraryForType = questionLibrary[mainType] || questionLibrary.unclear;
  const randomQuestionIndex = Math.floor(Math.random() * questionLibraryForType.length);
  const guidingQuestion = questionLibraryForType[randomQuestionIndex];

  // 选择海报标题和底部文案
  const titleIndex = Math.floor(Math.random() * posterTitles.length);
  const footerIndex = Math.floor(Math.random() * posterFooters.length);

  return {
    title: posterTitles[titleIndex],
    summaries: summaries,
    guidingQuestion: guidingQuestion,
    footer: posterFooters[footerIndex],
    timestamp: new Date().toLocaleString('zh-CN'),
    recordTypes: recordTypes
  };
}
